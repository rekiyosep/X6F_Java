    protected void datatable() {
        Object[] baris = {"No Identitas", "Nama Pasien", "Jenis Kelamin", "Alamat", "Golongan Darah"};
        tabmode = new DefaultTableModel(null, baris);
        tbl_pasien.setModel(tabmode);

        String sql = "select * from pasien order by id desc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String a = hasil.getString("id");
                String b = hasil.getString("nama");
                String c = hasil.getString("jk");
                String d = hasil.getString("alamat");
                String e = hasil.getString("goldar");

                String[] data = {a, b, c, d, e};
                tabmode.addRow(data);
            }
        } catch (Exception e) {

        }
    }

    private void caridata(String key) {
        Object[] baris = {"No Identitas", "Nama Pasien", "Jenis Kelamin", "Alamat", "Golongan Darah"};
        tabmode = new DefaultTableModel(null, baris);
        tbl_pasien.setModel(tabmode);

        String sql = "select * from pasien where id LIKE '%" + key + "%' OR nama LIKE '%" + key + "%' OR jk LIKE '%" + key + "%' OR alamat LIKE '%" + key + "%' OR goldar LIKE '%" + key + "%' order by id desc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String a = hasil.getString("id");
                String b = hasil.getString("nama");
                String c = hasil.getString("jk");
                String d = hasil.getString("alamat");
                String e = hasil.getString("goldar");

                String[] data = {a, b, c, d, e};
                tabmode.addRow(data);
            }
        } catch (Exception e) {

        }

    }


