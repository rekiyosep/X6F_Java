    private void tbl_pasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_pasienMouseClicked
        // TODO add your handling code here:
        int bar = tbl_pasien.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();

        tid.setText(a);
        tnama.setText(b);

        if (c.equals("Laki-laki")) {
            rlaki.setSelected(true);
            rperem.setSelected(false);
        } else {
            rlaki.setSelected(false);
            rperem.setSelected(true);
        }
        talamat.setText(d);
        cgol.setSelectedItem(e);

    }//GEN-LAST:event_tbl_pasienMouseClicked

